package org.operators;

public class Main {
    public static void main(String[] args) {
        int a = 65;
        int b = 25;
        double ak47 = 2.32;
        double m4 = 4;

        System.out.println(b + a);
        System.out.println(b / a);
        System.out.println(b * a);
        System.out.println(b % a);

        System.out.println(ak47 + m4);
        System.out.println(ak47 / m4);
        System.out.println(ak47 * m4);
        System.out.println(ak47 % m4);

        a = a + 1;
        System.out.println(a);

        a++;
        System.out.println(a);

    }
}